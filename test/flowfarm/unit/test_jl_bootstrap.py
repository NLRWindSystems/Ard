class TestIsManifestMismatchError:

    @pytest.mark.parametrize(
        "msg",
        [
            "manifest resolved with a different julia version",
            "Manifest generated by a different version of Julia",
            "Could not locate the source code for the StyledStrings package",
            "DIFFERENT JULIA VERSION",  # case-insensitive
            "prefix text: different julia version :suffix",
        ],
    )
    def test_returns_true_for_known_markers(self, msg):
        assert bootstrap._is_manifest_mismatch_error(Exception(msg)) is True

    def test_returns_false_for_unrelated_error(self):
        assert (
            bootstrap._is_manifest_mismatch_error(Exception("FLOWFarm not found"))
            is False
        )

    def test_returns_false_for_empty_message(self):
        assert bootstrap._is_manifest_mismatch_error(Exception("")) is False


