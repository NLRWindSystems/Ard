# _normalize_juliacall_env_vars
# ---------------------------------------------------------------------------


class TestNormalizeJuliacallEnvVars:

    def test_strips_project_and_exe_and_warns(self, monkeypatch):
        monkeypatch.setenv("PYTHON_JULIACALL_PROJECT", "/some/project")
        monkeypatch.setenv("PYTHON_JULIACALL_EXE", "julia")
        monkeypatch.delenv("ARD_FLOWFARM_RESPECT_JULIACALL_ENV", raising=False)

        with warnings.catch_warnings(record=True) as caught:
            warnings.simplefilter("always")
            bootstrap._normalize_juliacall_env_vars()

        assert len(caught) == 1
        assert "Ignoring" in str(caught[0].message)
        assert "PYTHON_JULIACALL_PROJECT" not in os.environ
        assert "PYTHON_JULIACALL_EXE" not in os.environ

    def test_strips_only_project_when_exe_absent(self, monkeypatch):
        monkeypatch.setenv("PYTHON_JULIACALL_PROJECT", "/some/project")
        monkeypatch.delenv("PYTHON_JULIACALL_EXE", raising=False)
        monkeypatch.delenv("ARD_FLOWFARM_RESPECT_JULIACALL_ENV", raising=False)

        with warnings.catch_warnings(record=True) as caught:
            warnings.simplefilter("always")
            bootstrap._normalize_juliacall_env_vars()

        assert len(caught) == 1
        assert "PYTHON_JULIACALL_PROJECT" not in os.environ

    def test_respects_opt_in_flag(self, monkeypatch):
        monkeypatch.setenv("ARD_FLOWFARM_RESPECT_JULIACALL_ENV", "1")
        monkeypatch.setenv("PYTHON_JULIACALL_EXE", "julia +1.10")

        with warnings.catch_warnings(record=True) as caught:
            warnings.simplefilter("always")
            bootstrap._normalize_juliacall_env_vars()

        assert len(caught) == 0
        assert os.environ["PYTHON_JULIACALL_EXE"] == "julia +1.10"

    def test_noop_when_no_vars_set(self, monkeypatch):
        monkeypatch.delenv("PYTHON_JULIACALL_PROJECT", raising=False)
        monkeypatch.delenv("PYTHON_JULIACALL_EXE", raising=False)
        monkeypatch.delenv("ARD_FLOWFARM_RESPECT_JULIACALL_ENV", raising=False)

        with warnings.catch_warnings(record=True) as caught:
            warnings.simplefilter("always")
            bootstrap._normalize_juliacall_env_vars()

        assert len(caught) == 0


