class TestGetJuliaModule:

    def test_returns_module_object(self, mock_juliacall, monkeypatch):
        mock_juliacall.newmodule = MagicMock(return_value=MagicMock(name="ArdFLOWFarm"))
        monkeypatch.setitem(sys.modules, "juliacall", mock_juliacall)

        result = bootstrap.get_julia_module()

        mock_juliacall.newmodule.assert_called_once_with("ArdFLOWFarm")
        assert result is not None

    def test_singleton_returns_same_module(self, mock_juliacall, monkeypatch):
        mock_juliacall.newmodule = MagicMock(return_value=MagicMock(name="ArdFLOWFarm"))
        monkeypatch.setitem(sys.modules, "juliacall", mock_juliacall)

        result1 = bootstrap.get_julia_module()
        result2 = bootstrap.get_julia_module()

        assert result1 is result2
        mock_juliacall.newmodule.assert_called_once()  # not called twice
