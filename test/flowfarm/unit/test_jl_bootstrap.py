class TestEnsureFlowfarmLoaded:

    def test_activates_julia_env(self, mock_juliacall):
        bootstrap.ensure_flowfarm_loaded()
        mock_juliacall.Pkg.activate.assert_called_once()

    def test_instantiates_julia_env(self, mock_juliacall):
        bootstrap.ensure_flowfarm_loaded()
        mock_juliacall.Pkg.instantiate.assert_called_once()

    def test_calls_seval_to_load_flowfarm(self, mock_juliacall):
        bootstrap.ensure_flowfarm_loaded()
        mock_juliacall.Main.seval.assert_called_once_with("using FLOWFarm")

    def test_returns_jl_main(self, mock_juliacall):
        result = bootstrap.ensure_flowfarm_loaded()
        assert result is mock_juliacall.Main

    def test_does_not_reinitialize_on_second_call(self, mock_juliacall):
        bootstrap.ensure_flowfarm_loaded()
        mock_juliacall.Pkg.activate.reset_mock()
        mock_juliacall.Pkg.instantiate.reset_mock()

        bootstrap.ensure_flowfarm_loaded()

        mock_juliacall.Pkg.activate.assert_not_called()
        mock_juliacall.Pkg.instantiate.assert_not_called()

    def test_rebuilds_manifest_on_version_mismatch(self, mock_juliacall, monkeypatch):
        mock_juliacall.Pkg.instantiate.side_effect = Exception(
            "manifest resolved with a different julia version"
        )
        rebuild_mock = MagicMock()
        monkeypatch.setattr(bootstrap, "_rebuild_flowfarm_env", rebuild_mock)

        with warnings.catch_warnings(record=True) as caught:
            warnings.simplefilter("always")
            bootstrap.ensure_flowfarm_loaded()

        rebuild_mock.assert_called_once()
        assert any("mismatch" in str(w.message).lower() for w in caught)

    def test_reraises_non_mismatch_errors(self, mock_juliacall):
        mock_juliacall.Pkg.instantiate.side_effect = RuntimeError("disk full")

        with pytest.raises(RuntimeError, match="disk full"):
            bootstrap.ensure_flowfarm_loaded()


