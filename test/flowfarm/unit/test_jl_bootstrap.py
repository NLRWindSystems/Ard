class TestRebuildFlowfarmEnv:

    def test_deletes_existing_manifest(self, tmp_path):
        manifest = tmp_path / "Manifest.toml"
        manifest.write_text("old manifest content")
        mock_pkg = MagicMock()

        bootstrap._rebuild_flowfarm_env(mock_pkg, tmp_path)

        assert not manifest.exists()

    def test_no_error_when_manifest_absent(self, tmp_path):
        mock_pkg = MagicMock()
        bootstrap._rebuild_flowfarm_env(mock_pkg, tmp_path)  # should not raise

    def test_adds_flowfarm_from_pinned_url(self, tmp_path):
        mock_pkg = MagicMock()

        bootstrap._rebuild_flowfarm_env(mock_pkg, tmp_path)

        mock_pkg.add.assert_called_once_with(
            url=bootstrap.FLOWFARM_GIT_URL,
            rev=bootstrap.FLOWFARM_REV,
        )

    def test_calls_resolve_and_instantiate(self, tmp_path):
        mock_pkg = MagicMock()

        bootstrap._rebuild_flowfarm_env(mock_pkg, tmp_path)

        mock_pkg.resolve.assert_called_once()
        mock_pkg.instantiate.assert_called_once()

    def test_rm_failure_is_silenced(self, tmp_path):
        mock_pkg = MagicMock()
        mock_pkg.rm.side_effect = RuntimeError("FLOWFarm not in environment")

        bootstrap._rebuild_flowfarm_env(mock_pkg, tmp_path)

        # add should still be called despite rm failing
        mock_pkg.add.assert_called_once()


